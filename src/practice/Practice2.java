package practice;

public class Practice2 {

	public static void main(String[] args) {
		// 演算時の型変換
		System.out.println(5/2);
		// 片方がdouble型の場合
		System.out.println(5.0/2);
		
		// 文字列を含む演算時の型変換
		System.out.println("私の年齢は" + 23 + "歳です");
		
		// 改行せずに文字を表示する
		String name = "歩実";
		System.out.print("私の名前は");
		System.out.print(name);
		System.out.print("です");

	}

}
